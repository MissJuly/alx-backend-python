#!/usr/bin/env python3
"""
Module that provides a function for concatenating
two strings.
"""


def concat(str1: str, str2: str):
    """
    parameters:
        str1 (str): the first string.
        str2 (str): the second string.
    returns:
        str: The concatenation of str1 and str2
    """
    return str1 + str2
